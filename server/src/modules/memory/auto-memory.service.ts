export const autoMemoryService = {
  /**
   * Check if affection crossed any milestone and create memory if so.
   */
  async checkAffinityMilestone(
    userId: string,
    characterId: string,
    previousAffection: number,
    newAffection: number
  ): Promise<void> {
    for (const milestone of AFFECTION_MILESTONES) {
      if (previousAffection < milestone && newAffection >= milestone) {
        const info = AFFECTION_MILESTONE_TITLES[milestone];
        try {
          // Check if milestone memory already exists to avoid duplicates
          const existing = await prisma.memory.findFirst({
            where: { userId, characterId, milestone: `affection_${milestone}` },
          });
          if (existing) continue;

          await prisma.memory.create({
            data: {
              userId,
              characterId,
              title: info.title,
              description: info.description,
              type: 'MILESTONE',
              milestone: `affection_${milestone}`,
              isAutoGenerated: true,
              autoGenSource: 'milestone',
            },
          });
          log.info(`Created affection milestone memory at ${milestone} for char ${characterId}`);
        } catch (error) {
          log.error(`Error creating milestone memory at ${milestone}:`, error);
        }
      }
    }
  },

  /**
   * Auto-create memory when relationship stage changes.
   */
  async createRelationshipStageMemory(
    userId: string,
    characterId: string,
    newStage: RelationshipStage
  ): Promise<void> {
    const info = STAGE_TITLES[newStage];
    if (!info) return;

    try {
      // Check if stage memory already exists to avoid duplicates
      const existing = await prisma.memory.findFirst({
        where: { userId, characterId, milestone: `stage_${newStage.toLowerCase()}` },
      });
      if (existing) return;

      await prisma.memory.create({
        data: {
          userId,
          characterId,
          title: info.title,
          description: info.description,
          type: 'MILESTONE',
          milestone: `stage_${newStage.toLowerCase()}`,
          isAutoGenerated: true,
          autoGenSource: 'stage_change',
        },
      });
      log.info(`Created stage change memory: ${newStage} for char ${characterId}`);
    } catch (error) {
      log.error(`Error creating stage memory for ${newStage}:`, error);
    }
  },

  /**
   * Auto-create memory when leveling up (only at milestone levels: 5, 10, 15...).
   */
  async createLevelUpMemory(
    userId: string,
    characterId: string,
    newLevel: number
  ): Promise<void> {
    if (newLevel % 5 !== 0) return;

    try {
      const existing = await prisma.memory.findFirst({
        where: { userId, characterId, milestone: `level_${newLevel}` },
      });
      if (existing) return;

      await prisma.memory.create({
        data: {
          userId,
          characterId,
          title: `Đạt Level ${newLevel}! 🎉`,
          description: `Cùng nhau đạt đến level ${newLevel} - mối quan hệ ngày càng sâu sắc và bền chặt hơn.`,
          type: 'EVENT',
          milestone: `level_${newLevel}`,
          isAutoGenerated: true,
          autoGenSource: 'level_up',
        },
      });
      log.info(`Created level up memory at level ${newLevel} for char ${characterId}`);
    } catch (error) {
      log.error(`Error creating level up memory at ${newLevel}:`, error);
    }
  },
};
