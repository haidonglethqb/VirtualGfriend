/**
 * Auto Memory Service
 * Automatically creates Memory records from key events:
 * - Affection milestones (100, 300, 500, 700, 900)
 * - Relationship stage changes
 * - Level milestones (every 5 levels)
 */

import { prisma } from '../../lib/prisma';
import { RelationshipStage } from '@prisma/client';
import { createModuleLogger } from '../../lib/logger';

const log = createModuleLogger('AutoMemory');

const AFFECTION_MILESTONES = [100, 300, 500, 700, 900];

